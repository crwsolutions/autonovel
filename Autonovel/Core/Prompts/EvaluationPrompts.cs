        public static string BuildChapterEvaluationPrompt(string voice, string world, string characters, string canon, string chapterOutline, string prevChapterTail, string chapterText)
        {
            var sb = new StringBuilder();
            sb.AppendLine("Evaluate this fantasy novel chapter against the planning docs.");
            sb.AppendLine();
            sb.AppendLine("SCORING CALIBRATION:");
            sb.AppendLine("  9-10: Among the best chapters you've read in published fantasy. Name");
            sb.AppendLine("        a specific published chapter it competes with, or don't give 9+.");
            sb.AppendLine("  7-8:  Strong, publishable with editorial polish. Specific flaws exist");
            sb.AppendLine("        but don't break the reading experience.");
            sb.AppendLine("  5-6:  Functional but flat. A competent draft that needs substantial revision.");
            sb.AppendLine("        Generic where it should be specific. Safe where it should risk.");
