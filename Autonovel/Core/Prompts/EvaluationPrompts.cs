            sb.AppendLine("  \"plants_seeded\": {\"score\": N, \"weakest_moment\": \"...\", \"fix\": \"...\", \"note\": \"...\"},");
            sb.AppendLine("  \"prose_quality\": {\"score\": N, \"weakest_sentence\": \"quote it\", \"fix\": \"rewrite suggestion\", \"strongest_sentence\": \"quote it\", \"note\": \"...\"},");
            sb.AppendLine("  \"continuity\": {\"score\": N, \"note\": \"...\"},");
            sb.AppendLine("  \"canon_compliance\": {\"score\": N, \"violations\": [\"list any found\"], \"note\": \"...\"},");
            sb.AppendLine("  \"lore_integration\": {\"score\": N, \"weakest_moment\": \"...\", \"fix\": \"...\", \"note\": \"...\"},");
            sb.AppendLine("  \"engagement\": {\"score\": N, \"weakest_moment\": \"...\", \"fix\": \"...\", \"note\": \"...\"},");
            sb.AppendLine("  \"three_weakest_sentences\": [\"quote 1\", \"quote 2\", \"quote 3\"],");
            sb.AppendLine("  \"three_strongest_sentences\": [\"quote 1\", \"quote 2\", \"quote 3\"],");
            sb.AppendLine("  \"ai_patterns_detected\": [\"list any AI writing patterns found\"],");
            sb.AppendLine("  \"overall_score\": N,");
            sb.AppendLine("  \"weakest_dimension\": \"...\",");
            sb.AppendLine("  \"top_3_revisions\": [\"specific, actionable revision 1\", \"revision 2\", \"revision 3\"],");
