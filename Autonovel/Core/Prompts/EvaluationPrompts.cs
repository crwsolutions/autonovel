            sb.AppendLine("  1-2:  Not usable. Rewrite from scratch.");
            sb.AppendLine();
            sb.AppendLine("  The MEDIAN score for a competent AI-generated chapter should be 6.");
            sb.AppendLine("  A 7 means it does something a generic AI draft wouldn't.");
            sb.AppendLine("  An 8 means a human editor would keep it with minor notes.");
            sb.AppendLine("  Most dimensions should score 6-7. Reserve 8+ for genuine excellence.");
            sb.AppendLine();
            sb.AppendLine("MANDATORY: For each dimension, you must identify:");
            sb.AppendLine("  (a) The single WEAKEST MOMENT -- quote the specific sentence or passage");
            sb.AppendLine("  (b) What would make it better -- a concrete revision, not a vague note");
            sb.AppendLine("  If every sentence is perfect, you're not reading carefully enough.");
            sb.AppendLine();
            sb.AppendLine("VOICE DEFINITION:");
            sb.AppendLine(voice);
            sb.AppendLine();
            sb.AppendLine("WORLD BIBLE (summary):");
            sb.AppendLine(world);
            sb.AppendLine();
            sb.AppendLine("CHARACTER REGISTRY:");
            sb.AppendLine(characters);
            sb.AppendLine();
            sb.AppendLine("CANON (established hard facts -- violations are bugs):");
            sb.AppendLine(canon);
            sb.AppendLine();
            sb.AppendLine("CHAPTER OUTLINE ENTRY:");
            sb.AppendLine(chapterOutline);
            sb.AppendLine();
            sb.AppendLine("PREVIOUS CHAPTER (last 1500 words):");
            sb.AppendLine(prevChapterTail);
            sb.AppendLine();
            sb.AppendLine("THE CHAPTER TO EVALUATE:");
            sb.AppendLine(chapterText);
            sb.AppendLine();
            sb.AppendLine("CROSS-CHECKS (perform before scoring):");
            sb.AppendLine("1. QUOTE TEST: Find the 3 best sentences and 3 weakest sentences.");
            sb.AppendLine("   If you can't find 3 weak ones, lower your standards -- every");
            sb.AppendLine("   chapter has weak moments. Look for: generic phrasing where");
            sb.AppendLine("   specificity was possible, rhythmic monotony in any paragraph,");
            sb.AppendLine("   metaphors that don't come from the character's experience,");
            sb.AppendLine("   emotional moments that tell instead of show, transitions that");
            sb.AppendLine("   summarize instead of dramatize.");
            sb.AppendLine("2. DIALOGUE REALISM: Read all dialogue aloud (mentally). Does it");
            sb.AppendLine("   sound like speech or like written prose? Do characters say things");
            sb.AppendLine("   a 14-year-old / 60-year-old / etc. would actually say?");
            sb.AppendLine("3. SCENE VS SUMMARY: How much of the chapter is in-scene (moment");
            sb.AppendLine("   by moment, with dialogue and action) vs summary (narrator");
            sb.AppendLine("   compressing time)? Chapters heavy on summary score lower on");
            sb.AppendLine("   engagement regardless of prose quality.");
            sb.AppendLine("4. AI PATTERN CHECK: Look for these common AI writing patterns:");
            sb.AppendLine("   - Every paragraph the same length");
            sb.AppendLine("   - Observations always in threes (X, Y, and Z)");
            sb.AppendLine("   - Emotional beats that arrive on schedule rather than surprising");
            sb.AppendLine("   - Characters who never say the wrong thing or talk past each other");
            sb.AppendLine("   - Description that catalogs instead of selecting (listing 5 sensory");
            sb.AppendLine("     details when 2 specific ones would be sharper)");
            sb.AppendLine("   - Internal monologue explaining what the scene already showed");
            sb.AppendLine("5. EARNED VS GIVEN: Is tension earned through scene work or handed to");
            sb.AppendLine("   the reader through the narrator's assertions? Is mystery maintained");
            sb.AppendLine("   through genuine withholding or through the character conveniently");
            sb.AppendLine("   not thinking about things they'd think about?");
            sb.AppendLine();
            sb.AppendLine("Score these dimensions:");
            sb.AppendLine();
            sb.AppendLine("- voice_adherence: Does the prose match voice.md Part 2? Check: sentence");
            sb.AppendLine("  rhythm variation, vocabulary wells, body-before-emotion principle,");
            sb.AppendLine("  the specific tone described. Quote the strongest voice moment AND");
            sb.AppendLine("  the weakest. Does ANY passage sound like generic fantasy prose that");
            sb.AppendLine("  could appear in any novel? If yes, score 7 max.");
            sb.AppendLine();
            sb.AppendLine("- beat_coverage: Did it hit every beat from the outline? Were beats");
            sb.AppendLine("  dramatized or merely mentioned? A beat that's summarized in a sentence");
            sb.AppendLine("  instead of lived in a scene counts as half-hit. Score reflects");
            sb.AppendLine("  QUALITY of beat execution, not just presence.");
            sb.AppendLine();
            sb.AppendLine("- character_voice: Remove all dialogue tags mentally. Can you tell who's");
            sb.AppendLine("  speaking? Do characters ever sound alike? Does dialogue read as speech");
            sb.AppendLine("  or as written prose? Does Cass sound like a specific 14-year-old, or");
            sb.AppendLine("  like \"young protagonist\"? Does anyone say something surprising -- not");
            sb.AppendLine("  just the right thing, but a REAL thing? Characters who never stumble,");
            sb.AppendLine("  hesitate, or say something slightly wrong are AI-pattern characters.");
            sb.AppendLine();
            sb.AppendLine("- plants_seeded: Were foreshadowing elements placed naturally? A plant");
            sb.AppendLine("  that's obvious is worse than a plant that's invisible. Score based on");
            sb.AppendLine("  HOW WELL they're integrated, not just whether they're present.");
            sb.AppendLine();
            sb.AppendLine("- prose_quality: Sentence variety (measure: do 3+ consecutive sentences");
            sb.AppendLine("  start the same way?). Specificity (concrete nouns > abstract).");
            sb.AppendLine("  Metaphors from Cass's experience, not from a thesaurus. Show-don't-tell");
            sb.AppendLine("  at emotional peaks. QUOTE the weakest sentence and explain why. Also");
            sb.AppendLine("  check for: repeated phrases, leaned-on constructions, paragraphs that");
            sb.AppendLine("  could be cut without loss.");
            sb.AppendLine();
            sb.AppendLine("- continuity: Does it follow logically from the previous chapter? Emotional");
            sb.AppendLine("  continuity as well as plot continuity. Does the character's state of");
            sb.AppendLine("  mind track?");
            sb.AppendLine();
            sb.AppendLine("- canon_compliance: Check ALL facts against canon. List violations.");
            sb.AppendLine("  One major violation caps score at 6. Check: character names, locations,");
            sb.AppendLine("  magic system rules, timeline, established events, physical descriptions.");
            sb.AppendLine();
            sb.AppendLine("- lore_integration: Does the world do WORK in this chapter, or is it");
            sb.AppendLine("  set dressing? A scene that could happen in any fantasy city with");
            sb.AppendLine("  find-and-replace on proper nouns scores 5 max.");
            sb.AppendLine();
            sb.AppendLine("- engagement: Would a reader turn the page? Where does tension come from --");
            sb.AppendLine("  plot, character, mystery, prose? Is there a moment that SURPRISES?");
            sb.AppendLine("  Predictable excellence is still predictable. Score 8+ only if the");
            sb.AppendLine("  chapter does something unexpected.");
            sb.AppendLine();
            sb.AppendLine("Respond with JSON:");
            sb.AppendLine("{");
            sb.AppendLine("  \"voice_adherence\": {\"score\": N, \"weakest_moment\": \"quote the specific weak passage\", \"fix\": \"how to improve it\", \"note\": \"...\"},");
            sb.AppendLine("  \"beat_coverage\": {\"score\": N, \"weakest_moment\": \"...\", \"fix\": \"...\", \"note\": \"...\"},");
